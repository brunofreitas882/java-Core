package entities;

public class Caminhao {
	String marca;
	int eixos;
	int toneladas;
	int quantidade;
	
	public Caminhao(String marca, int eixos, int toneladas) {
		this.marca = marca;
		this.eixos = eixos;
		this.toneladas = toneladas;
	}
	
	public void addCaminhao( ) {
		this.quantidade += quantidade;
	}
}
